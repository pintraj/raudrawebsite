                        <div class="course-des-content">
                            <p>Apache Hadoop is an open source software framework for storage and large scale processing of data-sets on clusters of commodity hardware. Hadoop is an Apache top-level project being built and used by a global community of contributors and users. Hadoop was created by Doug Cutting and Mike Cafarella in 2005. It was originally developed to support distribution for the Nutch search engine project. Doug, who was working at Yahoo! at the time and is now Chief Architect of Cloudera, named the project after his son's toy elephant.</p> 
                            <p><strong>Hadoop Common:</strong> contains libraries and utilities needed by other Hadoop modules</p>
                            <p><strong>Hadoop Distributed File System (HDFS):</strong> a distributed file-system that stores data on the commodity machines, providing very high aggregate bandwidth across the cluster</p>
                            <p><strong>Hadoop YARN:</strong> a resource-management platform responsible for managing compute resources in clusters and using them for scheduling of users' applications </p>
                            <p><strong>Hadoop MapReduce:</strong> a programming model for large scale data processing</p>
                            <p>All the modules in Hadoop are designed with a fundamental assumption that hardware failures (of individual machines, or racks of machines) are common and thus should be automatically handled in software by the framework. Apache Hadoop's MapReduce and HDFS components originally derived respectively from Google's MapReduce and Google File System (GFS) papers.</p>
                            <p>Beyond HDFS, YARN and MapReduce, the entire Apache Hadoop "platform" is now commonly considered to consist of a number of related projects as well: Apache Pig, Apache Hive, Apache HBase, and others.</p>

